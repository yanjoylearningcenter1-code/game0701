export function getUiLang() {
  const stored = typeof localStorage !== "undefined" ? localStorage.getItem(LANG_KEY) : null;
  if (stored === "zh") return "zh-HK";
  if (stored && stored !== "system" && DICT[stored]) return stored;
  return "en";
}

export function setUiLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
  window.dispatchEvent(new Event(LANG_CHANGE_EVENT));
}

/** Replace {key} placeholders in translated strings. */
export function formatT(str, vars = {}) {
  return String(str).replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? String(vars[k]) : `{${k}}`));
}

export function useLang() {
  const [lang, setLang] = useState(getUiLang());
  useEffect(() => {
    const onChange = () => setLang(getUiLang());
    window.addEventListener(LANG_CHANGE_EVENT, onChange);
    window.addEventListener("storage", onChange);
    return () => {
      window.removeEventListener(LANG_CHANGE_EVENT, onChange);
      window.removeEventListener("storage", onChange);
    };
  }, []);
  const t = (key, vars) => {
    const raw = (DICT[lang] && DICT[lang][key]) || DICT.en[key] || key;
    return vars ? formatT(raw, vars) : raw;
  };
  return { lang, t };
}
