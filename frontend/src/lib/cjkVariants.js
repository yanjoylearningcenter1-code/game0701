const TRADITIONAL_TO_SIMPLIFIED = Object.fromEntries(
  Object.entries(SIMPLIFIED_TO_TRADITIONAL).map(([s, t]) => [t, s])
);

/** Canonical form for CJK answer comparison — prefers traditional. */
export function canonicalCjk(str) {
  return [...(str || "")].map((ch) => SIMPLIFIED_TO_TRADITIONAL[ch] || ch).join("");
}

/** True when two answers match after trim, punctuation strip, and trad/simp normalization. */
export function answersEquivalent(a, b) {
  const na = canonicalCjk((a || "").trim().replace(/[，。！？、,.!?\s]/g, "").toLowerCase());
  const nb = canonicalCjk((b || "").trim().replace(/[，。！？、,.!?\s]/g, "").toLowerCase());
  if (!na || !nb) return false;
  if (na === nb) return true;
  // Also accept if one is simplified mirror of the other char-by-char
  const sa = [...na].map((ch) => TRADITIONAL_TO_SIMPLIFIED[ch] || ch).join("");
  const sb = [...nb].map((ch) => TRADITIONAL_TO_SIMPLIFIED[ch] || ch).join("");
  return sa === sb || sa === nb || na === sb;
}
